                        <p>1.- Define su uso: lo primero que deberás hacer es definir el uso que le darás al espacio, ya que no es lo mismo un terreno para construir una casa, que un terreno para funcionar como bodega o depósito.
<br>
2.- Busca asesoría profesional: si tú no eres experto en el tema busca alguien que sí lo sea, requerirás de la opinión de un arquitecto, ingeniero, abogados y hasta notarios. Es más sencillo buscar los servicios inmobiliarios ya que ellos ya tienen en su equipo un grupo de profesionales además de que también tendrán terrenos en venta Querétaro, Morelos, Puebla o el estado de tu preferencia y te brindarán más opciones, incluso dependiendo de las necesidades que tengas pueden ofrecerte bodegas en renta Querétaro o en alguna otra parte del país.<br>

3.- Evalúa el valor comercial: verifica el valor comercial del terreno comparado con otros de la misma zona.<br>

4.- Considera los aspectos socio-ambientales: verifica cuál es la historia del terreno y del ambiente que lo rodea, así como la actividad que rodea la zona, para ello visita el lugar en días y horarios diferentes para darte una idea tránsito, los ruidos y la cantidad de personas que pasan por el lugar.<br>

5.- Evalúa vías de acceso: un aspecto que luego se deja de lado son las vías de acceso y los medios de transporte que se tienen para llegar el lugar, considera todo esto para saber si te conviene.<br>

6.- Condiciones climáticas: revisa las condiciones climáticas del lugar, es recomendable visitar el terreno por la noche o después de una gran lluvia para que veas cómo queda la zona y si es algo con lo que puedes lidiar y sobre todo para considerar al momento de la construcción.<br>

7.-Servicos públicos: otro aspecto importante, sobre todo cuando se trata de terrenos, es evaluar los servicios a los que se tendrá acceso,  como agua potable, electricidad, teléfono, alumbrado público o la recolección de desechos.<br>

8.- Escrituras: para que no haya problemas al momento de la compra primero asegúrate que el terreno tenga las escrituras en regla, que no tenga adeudos o algún problema legal que podría perjudicarte.<br>

9.-Terreno irregular: en ocasiones es probable que veas un terreno a un precio irresistible, pero quizá se trate de un terreno no regularizado, si es el caso es mejor optar por otra opción ya que la regularización podría llevar años.<br>

10.- Revisa los reglamentos de edificación: antes de adquirirlo asegúrate de que podrás usarlo y construir lo que tu deseas con base en los reglamentos vigentes de la zona.

